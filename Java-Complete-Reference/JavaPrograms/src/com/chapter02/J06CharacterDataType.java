package com.chapter02;

public class J06CharacterDataType {
    public static void main(String[] args) {

        char x = 88;    // ascii value of 88 => Y
        char y = 'Y';

        System.out.println("Values : " + x + " " + y + '\n');          // X Y

        System.out.println("Variable x Now : " + x);
        x += 2;
        System.out.println("After increment two : " + x);

    }
}
