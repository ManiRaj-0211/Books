package com.chapter02;

public class J01ByteShortDataType {
    public static void main(String[] args) {

        byte a = 123;       // -128 to 127          =>  Byte Data Type
        short b = 32123;    // -32,768 to 32767     => Short Data Type

        System.out.println("Value of Byte Data Type : " + a);
        System.out.println("Value of Short Data Type : " + b);

    }
}
